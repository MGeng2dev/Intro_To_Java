//  Area and perimeter of a circle
//  Write a program that displays the area and perimeter of a circle
//  that has a radius of 5.5 using the following formulas:
//  Perimeter = 2 x radius x pi
//  Area = radius x radius x pi

public class Ex01_08 {
   public static void main(String[] args) {
      System.out.println("Circle with radius = 5");
      System.out.println("Perimeter = 2 x radius x pi = " + 2 * 5.5 * 3.14159);
      System.out.println("Area = radius x radius x pi = " + 5.5 * 5.5 * 3.14159);
      
   }
}